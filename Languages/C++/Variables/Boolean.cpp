/*Booleans can be used as a flagship between true or false! 
Boolean can be initialized either externally such as 
bool x=true; (or) Internally as 
bool x(false); 
Usage of negative operand '!' negates the entire bool value*/
#include<iostream>
using namespace std;
int main(){
	bool x(true);
	cout<<x<<endl;
	cout<<!x<<endl;
}

